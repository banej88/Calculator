package com.Branko.Calculator;

public class Logic {
			
	
						
								public double plus(double a,double b) {
								
									
									return a+b;
								}
								
								public double divide(double a,double b) {
									
									return a/b;
								}
								
								public double minus(double a,double b) {
									
									return a-b;
								}
								
								public double times(double a,double b) {
									
									return a*b;
								}
								
								
						
}
